#include "push_swap.h"

int	ft_check_arg(char **av)
{
	int	i;
	int j;

	i = 1;
	while (av[i])
	{
		j = 0;
		while (av[i][j])
		{
			if (!ft_isdigit(av[i][j]) && av[i][j] != ' ')
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
